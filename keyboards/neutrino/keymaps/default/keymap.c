// DIP switch/encoder button functions
void dip_switch_update_mask_user(uint32_t state) { 
	/*
   	if(layer_state_is(_MEDIA)){
	   layer_move(_FUNCTION);
	}
	else{
		layer_move(_MEDIA);
	}
	*/
	static int layer = 0;	// variable to track layer
	
	// Encoder DIP Switch
	if (state & (1UL << 0)) {

		static int returnlayer = 0;
		if(!layer_state_is(_MEDIA)){
			returnlayer = layer; //layer to return to when encoder is pressed again
			layer_move(_MEDIA);	// Activate media keys when encoder is pressed
			layer = 0;
		}
		else{
			layer_move(returnlayer); //return to layer before encoder was pressed
			layer = returnlayer;
		}
	}
	// Layer Toggle DIP Switch
	if(state & (1UL << 1)){
		// cycle through layers
		layer++;	// increment layer counter
		layer = layer % LAYER_COUNT;	// check layer counter is in range of layers
		layer_clear();
		layer_on(layer);	//set next layer 
		
	}
	
	

}

//Encoder Functions
void encoder_update_user(uint8_t index, bool clockwise) {

    if (clockwise) {
    	tap_code(KC_VOLU);
    } else {
        tap_code(KC_VOLD);
    }

}


